`timescale 1ns/1ps

module dff_r(input CLK, input D, input RST_N, output reg Q);
  initial Q = 1'b0;
  always @(posedge CLK or negedge RST_N)
    if (!RST_N) Q <= 1'b0;
    else Q <= D;
endmodule

module xor2_g(input A, input B, output Y);
  assign Y = A ^ B;
endmodule

module xor4_g(input A, input B, input C, input D, output Y);
  assign Y = A ^ B ^ C ^ D;
endmodule

module and2_g(input A, input B, output Y);
  assign Y = A & B;
endmodule

